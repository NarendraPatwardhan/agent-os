//! normalization rules, not JavaScript implementation code. These entries are public integration
//! identifiers, names, summaries, icons, and source endpoints/spec URLs used by `/svc/adapters`.
//! Source snapshot: <https://github.com/RhysSullivan/executor> at
//! `44fff2f9db297676b1ec6e6cdabb46a9487a75c5`.
//!
//! executor also publishes an open-ended feed at `https://integrations.sh/api.json`. That is not
//! part of this baked deterministic table; check it later only when the adapter plane has an
//! explicit refresh, cache, and policy contract.

use serde::Serialize;

#[derive(Debug, Clone, Copy, Serialize, PartialEq, Eq)]
pub enum RegistryKind {
    #[serde(rename = "openapi")]
    OpenApi,
    #[serde(rename = "microsoft-graph")]
    MicrosoftGraph,
    #[serde(rename = "google-discovery")]
    GoogleDiscovery,
    #[serde(rename = "graphql")]
    GraphQl,
    #[serde(rename = "mcp-remote")]
    McpRemote,
}

#[derive(Debug, Clone, Copy, Serialize, PartialEq, Eq)]
pub struct RegistryEntry {
    pub id: &'static str,
    pub name: &'static str,
    pub summary: &'static str,
    pub kind: RegistryKind,
    pub url: Option<&'static str>,
    pub endpoint: Option<&'static str>,
    pub icon: Option<&'static str>,
    pub featured: bool,
    pub oauth_audience: Option<&'static str>,
    pub scopes: &'static [&'static str],
    pub exact_paths: &'static [&'static str],
    pub path_prefixes: &'static [&'static str],
    pub tag_prefixes: &'static [&'static str],
    /// Curated egress origins (`scheme://host[:port]`) this integration's tools call. The host derives
    /// a connection's credential-egress allowlist from this when the embedder omits `origins`, so the
    /// user only names the capability + key. Curated (our constant), not read from a live spec, so a
    /// tampered upstream spec cannot redirect the credential. Empty ⇒ the embedder must pass `origins`.
    pub servers: &'static [&'static str],
}

/// Set `servers` on an entry built by one of the kind constructors (which default it to `NONE`).
const fn with_servers(mut entry: RegistryEntry, servers: &'static [&'static str]) -> RegistryEntry {
    entry.servers = servers;
    entry
}

pub fn entries() -> &'static [RegistryEntry] {
    REGISTRY
}

pub fn find(id: &str) -> Option<&'static RegistryEntry> {
    REGISTRY.iter().find(|entry| entry.id == id)
}

