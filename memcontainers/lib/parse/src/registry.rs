#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn carries_executor_curated_registry() {
        assert_eq!(entries().len(), 85);
        assert_eq!(find("petstore").unwrap().kind, RegistryKind::OpenApi);
        assert_eq!(
            find("microsoft").unwrap().kind,
            RegistryKind::MicrosoftGraph
        );
        assert_eq!(
            find("google-gmail").unwrap().kind,
            RegistryKind::GoogleDiscovery
        );
        assert_eq!(find("anilist").unwrap().kind, RegistryKind::GraphQl);
        assert_eq!(find("deepwiki").unwrap().kind, RegistryKind::McpRemote);
        assert!(find("chrome-devtools").is_none());
    }

    #[test]
    fn registry_serializes_without_code() {
        let value = serde_json::to_value(entries()).unwrap();
        let text = serde_json::to_string(&value).unwrap();
        assert!(text.contains("\"kind\":\"openapi\""));
        assert!(text.contains("\"kind\":\"graphql\""));
        assert!(!text.contains("chrome-devtools"));
        assert!(!text.contains("\"command\""));
        assert!(text.contains("petstore3.swagger.io"));
        assert!(text.contains("https://graph.microsoft.com/v1.0"));
    }
}
